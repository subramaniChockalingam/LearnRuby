#Calculating age with variables

# Declaring and initialising variables
# using assignment operator =
# variable name should not be a key word
currentYear=2018
myYob=1995
broYob=1993

puts "My YOB : #{myYob} "
puts "Brother's YOB : #{broYob} "
puts "Calculating age"

puts "My age : #{currentYear-myYob}"
puts "Brother's age : #{currentYear-broYob} "